#include <iostream>

int main() {
	
	int a = 7;
	a++;
	std::cout << "Isi variable a : " << a << std::endl;
	int b = 7;
	++b;
	std::cout << "Isi variable b : " << b << std::endl;
	int c = 7;
	c--;
	std::cout << "Isi variable c : " << c << std::endl;
	int d = 7;
	--d;
	std::cout << "Isi variable d : " << d << std::endl;
	
}